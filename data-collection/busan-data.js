function amtForOrder(a, ord) {
  if (!a) return 0;
  if (a['0'] !== undefined) return a['0'];            // 순위무관
  for (let k = Math.min(ord, 5); k >= 1; k--)         // 셋째이상 = 3+ 근접값
    if (a[String(k)] !== undefined) return a[String(k)];
  return 0;
}
